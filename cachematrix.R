## Function uses lexical scoping to cache potentially time-consuming computation.
## It takes a square invertible matrix and gives inverse.

## It takes the matrix as argument and stores the inverse.

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setmatrix <- function(solve) m <<- solve
  getmatrix <- function() m
  list(set = set, get = get,
       setmatrix = setmatrix,
       getmatrix = getmatrix)
}


## CacheSolve checks whether the inverse is already calculated if so it extracts that value.
## If it is not calculated it calculates the inverse and sets the value.s

cacheSolve <- function(x, ...) {
  m <- x$getmatrix()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setmatrix(m)
  m
}
